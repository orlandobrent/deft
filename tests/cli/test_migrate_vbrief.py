def _write_speckit_plan(
    plan_path: Path,
    items: list[dict],
    edges: list[dict] | None = None,
) -> None:
    """Write a speckit-shaped plan.vbrief.json fixture."""
    plan_path.parent.mkdir(parents=True, exist_ok=True)
