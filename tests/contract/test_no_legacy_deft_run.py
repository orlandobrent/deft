def test_no_legacy_deft_run_substring_in_scope() -> None:
    """No in-scope file may contain the legacy ``deft/run`` substring (#992 PR1).

    The exclusion taxonomy is documented in this module's docstring. A
    failure here means either (a) a contract surface was missed during the
    PR1 string-swap, or (b) a new file was added to the repo that
    introduces a fresh ``deft/run`` reference outside the redirect-stub
    contract. Either case must be addressed before merge.
    """
    hits = _scan_legacy_token()
    assert not hits, (
        "Found in-scope `deft/run` references after the #992 PR1 contract "
        "flip; expected zero. Either flip the remaining occurrences to "
        "`.deft/core/run` or, if the file is intentionally out of scope, "
        "extend the exclusion set in this test (and document the rationale "
        "in the module docstring + PR1 scope deviation notes).\n"
        + "\n".join(f"  {p}:{n}: {text}" for p, n, text in hits)
    )


def test_gate_preamble_redirect_stubs_retain_legacy_token() -> None:
    """Stubs that carry the DEFT-PREAMBLE-V1 gate instruction MUST keep ``deft/run``.

    Two of the eight v0.19 -> v0.20 redirect stubs (`skills/deft-setup/SKILL.md`
    and `skills/deft-build/SKILL.md`) carry the `<!-- DEFT-PREAMBLE-V1 -->`
    line-1 gate-instruction block (cross-tested by
    ``tests/content/test_main_md_preamble.py`` against the canonical-vs-legacy
    split documented there) -- those two MUST retain the pre-flip
    ``deft/run`` token so v0.19 stale-AGENTS.md references still resolve
    through the gate. The other six redirect stubs are simple sentinel-only
    redirect bodies pointing at ``deft/QUICK-START.md`` and never carried
    the gate-instruction surface; their redirect contract is covered
    independently by ``tests/content/test_deprecated_skill_redirects.py``.
    """
    gate_carrying_stubs = (
        "skills/deft-setup/SKILL.md",
        "skills/deft-build/SKILL.md",
    )
    failures: list[str] = []
    for rel_path in gate_carrying_stubs:
        full = _REPO_ROOT / rel_path
        if not full.is_file():
            failures.append(f"{rel_path}: gate-carrying stub file missing")
            continue
        text = full.read_text(encoding="utf-8")
        if _LEGACY_TOKEN not in text:
            failures.append(
                f"{rel_path}: missing `deft/run` -- gate-instruction "
                f"redirect contract broken (#411 + #768)"
            )
    assert not failures, (
        "Gate-instruction-carrying redirect stubs MUST retain the legacy "
        "`deft/run` token (#411 v0.19 -> v0.20 bridge + #768 gate "
        "instruction):\n  " + "\n  ".join(failures)
    )


def test_canonical_token_present_in_documented_surfaces() -> None:
    """Spot-check that the canonical ``.deft/core/run`` token actually landed.

    Pinned against a small set of high-signal contract surfaces from the
    AC enumeration so a partial flip (e.g. only one of the two
    ``templates/agents-entry.md`` references swapped) fails this test
    independently of the broader ``test_no_legacy_deft_run_substring_in_scope``
    sweep. Picks one occurrence per surface; the broader sweep covers the
    full inventory.
    """
    canonical = ".deft/core/run"
    documented_surfaces = (
        "templates/agents-entry.md",
        "main.md",
        "README.md",
        "UPGRADING.md",
        "docs/ARCHITECTURE.md",
        "docs/BROWNFIELD.md",
        "skills/deft-directive-setup/SKILL.md",
    )
    failures: list[str] = []
    for rel_path in documented_surfaces:
        full = _REPO_ROOT / rel_path
        assert full.is_file(), f"Expected documented surface {rel_path} to exist"
        text = full.read_text(encoding="utf-8")
        if canonical not in text:
            failures.append(f"{rel_path}: missing `{canonical}`")
    assert not failures, (
        "AC-enumerated contract surfaces must carry the canonical "
        f"`{canonical}` token after #992 PR1:\n  " + "\n  ".join(failures)
    )
