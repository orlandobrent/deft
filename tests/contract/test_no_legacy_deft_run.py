from __future__ import annotations

import subprocess
from pathlib import Path

import pytest

_REPO_ROOT = Path(__file__).resolve().parents[2]

# The literal substring being asserted absent. Tracked as a top-level
# constant so a future maintainer can search for the canonical token
# without needing to re-derive it from prose.
_LEGACY_TOKEN = "deft/run"

# Directory-prefix exclusions. A tracked file path is excluded if it
# starts with any of these prefixes (forward-slash form, matched against
# `git ls-files` output which is always forward-slash on Windows too).
_EXCLUDED_DIR_PREFIXES = (
    # AC-mandated
    "history/archive/",
    "vbrief/completed/",
    # Beyond-AC scope deviations (documented in module docstring above)
    "history/",                  # broader: plan-* / implementation-* / archive/**
    "vbrief/active/",
    "vbrief/pending/",
    "vbrief/proposed/",
    "vbrief/cancelled/",
    "vbrief/schemas/",
    "cmd/deft-install/",
    # Tests document contract tokens (legacy + canonical) by necessity --
    # they pin the redirect-stub gate-instruction split, document the
    # canonical-vs-legacy contract in prose, and exercise both surfaces
    # via fixtures. Tests are infrastructure, not user-visible contract
    # surfaces; regression scope stops at production source / docs.
    "tests/",
)

# Exact-path exclusions (forward-slash form, repo-root-relative).
_EXCLUDED_EXACT_PATHS = frozenset(
    {
        # AC-mandated
        "CHANGELOG.md",
        "meta/lessons.md",
        # Beyond-AC scope deviations (documented in module docstring above)
        "vbrief/PROJECT-DEFINITION.vbrief.json",
        "ROADMAP.md",
        "SPECIFICATION.md",
        "session.txt",
        "session2.txt",
        # PR4 (#992 PR4): `run`, `tasks/framework.yml`, and
        # `events/registry.json` were flipped to `.deft/core/run`; they are
        # NO LONGER excluded. The CLI sibling wrappers below remain excluded
        # as a low-blast-radius follow-up.
        "run.py",
        "run.bat",
        # PR2 (#992 PR2): relocator advisory-grep retains `deft/run` as a
        # search constant for consumer-side hardcode detection. See module
        # docstring `Beyond-AC` block for the rationale.
        "scripts/relocate.py",
        "scripts/_relocate_states.py",
        # #1015 F1: UPGRADING.md is the canonical migration docs surface and
        # MUST reference both the legacy `deft/run` and canonical
        # `.deft/core/run` paths so consumers know what to migrate FROM
        # and TO. The F1 "Manual edits required after relocate" subsection
        # is the operator's worklist; the literal token is load-bearing.
        "UPGRADING.md",
    }
)

# The eight legacy v0.19 -> v0.20 redirect-stub paths. These intentionally
# retain ``deft/run`` references so stale ``AGENTS.md`` references still
# resolve into the deprecation-redirect contract (#411). The list mirrors
# ``tests/content/test_deprecated_skill_redirects.py::_REDIRECT_STUBS`` --
# any addition there MUST be reflected here as well.
_LEGACY_REDIRECT_STUB_PATHS = frozenset(
    {
        f"skills/{name}/SKILL.md"
        for name in (
            "deft-sync",
            "deft-setup",
            "deft-build",
            "deft-review-cycle",
            "deft-roadmap-refresh",
            "deft-swarm",
            "deft-pre-pr",
            "deft-interview",
        )
    }
)


def _is_excluded(rel_path: str) -> bool:
    """Return True iff ``rel_path`` (forward-slash, repo-root-relative) is excluded."""
    if rel_path in _EXCLUDED_EXACT_PATHS:
        return True
    if rel_path in _LEGACY_REDIRECT_STUB_PATHS:
        return True
    return any(rel_path.startswith(prefix) for prefix in _EXCLUDED_DIR_PREFIXES)


def _tracked_files() -> list[str]:
    """Return the list of repo-tracked files via ``git ls-files``.

    Forward-slash separators on every platform (``git ls-files`` output is
    POSIX-style on Windows too). Falls back to a recursive directory walk
    only if ``git`` is unreachable, which would itself be a CI failure
    surface but lets the test produce a coherent error rather than a stack
    trace from ``subprocess``.
    """
    try:
        result = subprocess.run(
            ["git", "ls-files"],
            cwd=_REPO_ROOT,
            check=True,
            capture_output=True,
            text=True,
            encoding="utf-8",
        )
    except (subprocess.SubprocessError, FileNotFoundError) as exc:
        pytest.fail(
            f"Could not enumerate tracked files via `git ls-files`: {exc!r}. "
            "The contract regression cannot run without VCS metadata."
        )
    return [
        line.strip()
        for line in result.stdout.splitlines()
        if line.strip()
    ]


def _scan_legacy_token() -> list[tuple[str, int, str]]:
    """Return ``[(path, line_number, line_text)]`` for every in-scope hit."""
    hits: list[tuple[str, int, str]] = []
    for rel_path in _tracked_files():
        if _is_excluded(rel_path):
            continue
        full = _REPO_ROOT / rel_path
        if not full.is_file():
            continue
        try:
            text = full.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            # Binary or unreadable -- not a contract-string surface.
            continue
        if _LEGACY_TOKEN not in text:
            continue
        for idx, line in enumerate(text.splitlines(), start=1):
            if _LEGACY_TOKEN in line:
                hits.append((rel_path, idx, line.rstrip()))
    return hits


